package chapter2;

/**
 * 変数
 * 基本データ型（プリミティブ型）
 */
public class Example04_Variable {
	public static void main(String[] args) {
		// データ型△変数名;
		int i = 1212;
		char c = 'a';
		boolean b = true;
		
		// ※文字列は基本データ型ではない
		String str = "Hello";
		
		System.out.println(i);
		
		// JavaではUnicodeが使われている
		char u = '\u4032';
	}
}
