package chapter2;

/**
 * 算術演算子
 *
 */
public class Example08_ArithmeticOperator {
	public static void main(String[] args) {
		
		/*
		 * 整数
		 */
		int num;
		num = 2 + 1; // num = 3
		num = 3 - 4; // num = 1
		num = 5 * 6; // num = 30
		num = 8 / 2; // num = 4
		num = 10 % 3; // num = 1
		System.out.println("num : " + num);
		
		/*
		 * 浮動小数点数
		 */
		double d;
		d = 2 + 1.2; // d = 3.2 
		d = 4 -2; // d = 2.0
		System.out.println("d : " + d);
		
	}
}
